Movie_Ticket = input("Tell me your age, and I will give you the price for your movie ticket.: ")
Movie_Ticket = int(Movie_Ticket)
if Movie_Ticket <= 3:
    print("\nYour ticket is free.")
if Movie_Ticket <= 12:
    print("\nYour ticket is $10.00.")
if Movie_Ticket >= 12:
    print("\nYour ticket is $15.00.")

































































































































































































































