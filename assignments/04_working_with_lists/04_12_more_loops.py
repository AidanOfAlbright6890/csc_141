"More Loops"
Pastas = ["spaghetti", "penne_a_la_vodka", "Alfredo", "Gnocchi", "Lasagna"]
print(Pastas[0:3])
print(Pastas[-3:])

"Defining a Tuple"
dimensions = (200, 50)
print(dimensions[0])
print(dimensions[1])

"Looping Through All Values in a Tuple"
dimensions = (200, 50)
for dimension in dimensions:
    print(dimension)

"Writing Over a Tuple"
dimensions = (200, 50)
print("Original dimensions:")
for dimension in dimensions:
    print(dimension)

dimensions = (400, 100)
print("\nModified dimensions:")
for dimension in dimensions:
    print(dimension)
















