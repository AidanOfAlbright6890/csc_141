"Seeing the World"
Locations = ['Tokyo', 'Las Vegas', 'New York City', 'Hawaii', 'Greece']
print(Locations)
print("Here is the list sorted in alphabetical order")
print(sorted(Locations))
print(sorted(Locations))
print("Here is the list sorted in reverse-alphabetical order")
Locations.sort(reverse=True)
print(Locations)
print("Here is the list sorted in reverse")
Locations = ['Tokyo', 'Las Vegas', 'New York City', 'Hawaii', 'Greece']
print(Locations)
Locations.reverse()
print(Locations)
print("Here is the list sorted in alphabetical order")
print(sorted(Locations))
print("Here is the list sorted in reverse-alphabetical order")
Locations.sort(reverse=True)
print(Locations)


